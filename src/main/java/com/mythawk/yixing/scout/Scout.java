package com.mythawk.yixing.scout;

public class Scout {

    private int id;
    private String shoes_name;
    private int shock;
    private int parcel;
    private int support;
    private int grip;
    private int durable;
    private float scout;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getShoes_name() {
        return shoes_name;
    }

    public void setShoes_name(String shoes_name) {
        this.shoes_name = shoes_name;
    }

    public int getShock() {
        return shock;
    }

    public void setShock(int shock) {
        this.shock = shock;
    }

    public int getParcel() {
        return parcel;
    }

    public void setParcel(int parcel) {
        this.parcel = parcel;
    }

    public int getSupport() {
        return support;
    }

    public void setSupport(int support) {
        this.support = support;
    }

    public int getGrip() {
        return grip;
    }

    public void setGrip(int grip) {
        this.grip = grip;
    }

    public int getDurable() {
        return durable;
    }

    public void setDurable(int durable) {
        this.durable = durable;
    }

    public float getScout() {
        return scout;
    }

    public void setScout(float scout) {
        this.scout = scout;
    }
}
